function radiobutton5_Callback(hObject, eventdata, handles)
%% 刚体位姿
set(handles.radiobutton5,'Value',1);
set(handles.radiobutton6,'Value',0);
dataReceiveHZ = str2double(get(handles.edit54,'string'));
global MyClient
openSDK = get(handles.radiobutton3,'value');
openSegmentData = get(handles.radiobutton5,'value');
countFrame = 0;
while openSDK == 1 && openSegmentData==1
     %接收数据包，没有收到就等待                       
    while MyClient.GetFrame().Result.Value ~= Result.Success
        fprintf( '.' );
    end% while
    
     % Count the number of subjects
    SubjectCount = MyClient.GetSubjectCount().SubjectCount;
    subjectPose = zeros(20,6);
    subjectName = cell(20,1);
    subjectShowNumber = 0;
    markOnePosition = zeros(10,3);
    for SubjectIndex = 1:SubjectCount
        % Get the subject name
        SubjectName = MyClient.GetSubjectName( SubjectIndex ).SubjectName;
        
        % Count the number of segments
        SegmentCount = MyClient.GetSegmentCount( SubjectName ).SegmentCount;
        
        markPartPosition = zeros(20,3);
        MarkerCountDouble = 0.0;
        subjectShowNumber = subjectShowNumber + 1;
        subjectName{subjectShowNumber}=SubjectName;
        subjectName{subjectShowNumber + 1}='欧拉角';
        subjectName{subjectShowNumber + 2}='Mark-1';
        subjectShowNumber = subjectShowNumber + 2;
     
        for SegmentIndex = 1:SegmentCount
            % Get the segment name
            SegmentName = MyClient.GetSegmentName( SubjectName, SegmentIndex ).SegmentName;

            % Count the number of markers
            % 取mark球中心作为刚体坐标
            MarkerCount = MyClient.GetMarkerCount( SubjectName ).MarkerCount;
            
            for MarkerIndex = 1:MarkerCount
                MarkerCountDouble = MarkerCountDouble + 1.0;
                % Get the marker name
                MarkerName = MyClient.GetMarkerName( SubjectName, MarkerIndex ).MarkerName;
                % Get the global marker translation
                Output_GetMarkerGlobalTranslation = MyClient.GetMarkerGlobalTranslation( SubjectName, MarkerName );
                if MarkerCountDouble == 1.0
                    markOnePosition(SubjectIndex,:) = [Output_GetMarkerGlobalTranslation.Translation(1), ...
                                                       Output_GetMarkerGlobalTranslation.Translation(2), ...
                                                       Output_GetMarkerGlobalTranslation.Translation(3)];
                end
                markPartPosition(MarkerCountDouble,:) = [Output_GetMarkerGlobalTranslation.Translation(1), ...
                                                         Output_GetMarkerGlobalTranslation.Translation(2), ...
                                                         Output_GetMarkerGlobalTranslation.Translation(3)];
            end
        end
        %刚体中心点坐标
        subjectPose(SubjectIndex,1:3) = [sum(markPartPosition(:,1)) / MarkerCountDouble, ...
                                         sum(markPartPosition(:,2)) / MarkerCountDouble, ...
                                         sum(markPartPosition(:,3)) / MarkerCountDouble];
        %刚体全局欧拉角   
        % Get the global segment rotation in EulerXYZ co-ordinates
        Output_GetSegmentGlobalRotationEulerXYZ = MyClient.GetSegmentGlobalRotationEulerXYZ( SubjectName, SegmentName );
        subjectPose(SubjectIndex,4:6) = [Output_GetSegmentGlobalRotationEulerXYZ.Rotation(1), ...
                                         Output_GetSegmentGlobalRotationEulerXYZ.Rotation(2), ...
                                         Output_GetSegmentGlobalRotationEulerXYZ.Rotation(3)];
    end
    %显示任意mark球位置
    showRandomPosition = get(handles.radiobutton7,'value');
    randomMarkPosition = zeros(1,3);
    if showRandomPosition == 1
        SubjectName = get(handles.edit55,'string');
        MarkerName = get(handles.edit56,'string');
        Output_GetMarkerGlobalTranslation = MyClient.GetMarkerGlobalTranslation( SubjectName, MarkerName );
        randomMarkPosition(1,:) = [Output_GetMarkerGlobalTranslation.Translation(1), ...
                                   Output_GetMarkerGlobalTranslation.Translation(2), ...
                                   Output_GetMarkerGlobalTranslation.Translation(3)];
        % 显示任意mark点名字与坐标
        set(handles.edit57,'string',MarkerName);
        set(handles.edit58,'string',randomMarkPosition(1,1));
        set(handles.edit59,'string',randomMarkPosition(1,2));
        set(handles.edit60,'string',randomMarkPosition(1,3));
    end
    
    % 显示数据接收频率与时延
    set(handles.edit19,'string',MyClient.GetFrameRate().FrameRateHz);
    set(handles.edit20,'string',MyClient.GetLatencyTotal().Total);
    
    % 显示刚体的名字
    set(handles.edit1,'string',subjectName{1});
    set(handles.edit3,'string',subjectName{2});
    set(handles.edit5,'string',subjectName{3});
    set(handles.edit7,'string',subjectName{4});
    set(handles.edit9,'string',subjectName{5});
    set(handles.edit11,'string',subjectName{6});
    set(handles.edit13,'string',subjectName{7});
    set(handles.edit15,'string',subjectName{8});
    set(handles.edit23,'string',subjectName{9});
    
    % 显示刚体位姿
    %显示Mark点坐标-第一个刚体
    set(handles.edit25,'string',subjectPose(1,1));
    set(handles.edit26,'string',subjectPose(1,2));
    set(handles.edit27,'string',subjectPose(1,3));
    
    set(handles.edit28,'string',subjectPose(1,4));
    set(handles.edit36,'string',subjectPose(1,5));
    set(handles.edit44,'string',subjectPose(1,6));
    
    set(handles.edit29,'string',markOnePosition(1,1));
    set(handles.edit37,'string',markOnePosition(1,2));
    set(handles.edit45,'string',markOnePosition(1,3));
     %显示Mark点坐标-第二个刚体
    set(handles.edit30,'string',subjectPose(2,1));
    set(handles.edit38,'string',subjectPose(2,2));
    set(handles.edit46,'string',subjectPose(2,3));
    
    set(handles.edit31,'string',subjectPose(2,4));
    set(handles.edit39,'string',subjectPose(2,5));
    set(handles.edit47,'string',subjectPose(2,6));
    
    set(handles.edit32,'string',markOnePosition(2,1));
    set(handles.edit40,'string',markOnePosition(2,2));
    set(handles.edit48,'string',markOnePosition(2,3));
     %显示Mark点坐标-第三个刚体
    set(handles.edit33,'string',subjectPose(3,1));
    set(handles.edit41,'string',subjectPose(3,2));
    set(handles.edit49,'string',subjectPose(3,3));
    
    set(handles.edit34,'string',subjectPose(3,4));
