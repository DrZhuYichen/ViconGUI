% --- Executes on button press in radiobutton3.
function radiobutton3_Callback(hObject, eventdata, handles)
%% 初始化Vicon SDK 
set(handles.radiobutton3,'Value',1);
set(handles.radiobutton4,'Value',0);

% Load the SDK
% hfile = fullfile(matlabroot,'extern','ViconDataStreamSDK_MATLAB.h','matrix.h');
% loadlibrary('libmx',hfile,'mfilename','mxproto')
Client.LoadViconDataStreamSDK();

% Program options
% 设置IP地址
modeServer = get(handles.radiobutton1,'value');
if modeServer==1
    HostName = 'localhost:801';
else
    clientIP = get(handles.edit18,'string');
%     HostName = clientIP;
    HostName = clientIP;
end

% Make a new client
global MyClient
MyClient = Client();

% Connect to a server
fprintf( 'Connecting to %s ...', HostName );
set(handles.edit62,'string','Connecting Host...');
while ~MyClient.IsConnected().Connected
  % Direct connection
  MyClient.Connect( HostName );
  
  fprintf( '.' );
end
fprintf( '\n' );

% Enable some different data types
MyClient.EnableSegmentData();
MyClient.EnableMarkerData();
MyClient.EnableUnlabeledMarkerData();
MyClient.EnableDeviceData();

% Set the streaming mode
MyClient.SetStreamMode( StreamMode.ClientPull );
% MyClient.SetStreamMode( StreamMode.ClientPullPreFetch );
% MyClient.SetStreamMode( StreamMode.ServerPush );

% Set the global up axis
MyClient.SetAxisMapping( Direction.Forward, ...
                         Direction.Left,    ...
                         Direction.Up );    % Z-up
% MyClient.SetAxisMapping( Direction.Forward, ...
%                          Direction.Up,      ...
%                          Direction.Right ); % Y-up

Output_GetAxisMapping = MyClient.GetAxisMapping();
fprintf( 'Axis Mapping: X-%s Y-%s Z-%s\n', Output_GetAxisMapping.XAxis.ToString(), ...
                                           Output_GetAxisMapping.YAxis.ToString(), ...
                                           Output_GetAxisMapping.ZAxis.ToString() );


% Discover the version number
Output_GetVersion = MyClient.GetVersion();
fprintf( 'Version: %d.%d.%d\n', Output_GetVersion.Major, ...
                                Output_GetVersion.Minor, ...
                                Output_GetVersion.Point );

%接受数据包，没有收到就等待                       
fprintf( 'Waiting for new frame...' );
while MyClient.GetFrame().Result.Value ~= Result.Success
    fprintf( '.' );
end% while
fprintf( '\n' );
                            
% 显示数据接收频率与时延
set(handles.edit19,'string',MyClient.GetFrameRate().FrameRateHz);
set(handles.edit20,'string',MyClient.GetLatencyTotal().Total);

function radiobutton4_Callback(hObject, eventdata, handles)
%% 关闭Vicon SDK
set(handles.radiobutton3,'Value',0);
set(handles.radiobutton4,'Value',1);

global MyClient
% Disconnect and dispose
MyClient.Disconnect();

% Unload the SDK
fprintf( 'Unloading SDK...' );
set(handles.edit62,'string','Unloading SDK...');
Client.UnloadViconDataStreamSDK();

fprintf( 'done\n' );

